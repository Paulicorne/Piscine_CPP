#include "Data.hpp"

/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/

Data::Data() : _i(0), _c('!')
{
}

Data::Data( const Data & src ) : _i(src._i), _c(src._c)
{
}


/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

Data::~Data()
{
}


/*
** --------------------------------- OVERLOAD ---------------------------------
*/

Data &				Data::operator=( Data const & rhs )
{
	if ( this != &rhs )
	{
		this->_i = rhs._i;
		this->_c = rhs._c;
	}
	return *this;
}

